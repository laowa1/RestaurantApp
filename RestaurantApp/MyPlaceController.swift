import UIKit


class MyPlaceController: UITableViewController {
    
    let place = PlaceModel.getPlaces()

    override func viewDidLoad() {
        super.viewDidLoad()

    }

    // MARK: - Table view data source

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        
        return place.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cell", for: indexPath) as! CustomTableViewCell
        let places = place[indexPath.row]
        cell.nameOfPlace.text = places.name
        cell.locationOfPlace.text = places.location
        cell.typeOfPlace.text = places.type
        cell.imageOfPlace.layer.cornerRadius = cell.imageOfPlace.frame.height / 2
        cell.imageOfPlace.image = UIImage(named: places.image)
        
        return cell
    }

